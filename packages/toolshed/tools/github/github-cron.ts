/**
 * Create a nightly benchmark cron workflow.
 * Runs benchmarks at 2AM UTC and creates issues on failure.
 */
export async function benchmarkCron(): Promise<void> {
  await scheduleGitHubAction(
    "nightly-benchmarks",
    "0 2 * * *",
    `bun run benchmarks/autoresearch/harness.ts 2>&1 | tee benchmark-output.log
EXIT_CODE=\${PIPESTATUS[0]}
if [ $EXIT_CODE -ne 0 ]; then
  gh issue create \\
    --title "Nightly benchmark failure $(date +%Y-%m-%d)" \\
    --body "$(cat benchmark-output.log | tail -100)" \\
    --label "benchmark-failure,automated"
fi`
  );
}

/**
 * Create a weekly dependency update check workflow.
 * Runs every Monday at 9AM UTC.
 */
export async function dependencyUpdateCron(): Promise<void> {
  await scheduleGitHubAction(
    "weekly-dependency-check",
    "0 9 * * 1",
    `echo "## Dependency Update Report" > dep-report.md
echo "" >> dep-report.md
echo "### Outdated packages" >> dep-report.md
bun outdated 2>&1 >> dep-report.md || true
echo "" >> dep-report.md
echo "### Security audit" >> dep-report.md
bun audit 2>&1 >> dep-report.md || true
echo "" >> dep-report.md
echo "*Generated on $(date)*" >> dep-report.md

gh issue create \\
  --title "Weekly dependency report $(date +%Y-%m-%d)" \\
  --body "$(cat dep-report.md)" \\
  --label "dependencies,automated"`
  );
}

/**
 * Create a monthly stale issue cleanup workflow.
 * Runs on the 1st of each month at 6AM UTC.
 */
export async function staleIssueCron(): Promise<void> {
  await scheduleGitHubAction(
    "monthly-stale-issues",
    "0 6 1 * *",
    `# Close issues with no activity for 90+ days
STALE_ISSUES=$(gh issue list --state open --json number,updatedAt --jq '.[] | select((.updatedAt | fromdateiso8601) < (now - 7776000)) | .number')

for issue in $STALE_ISSUES; do
  gh issue comment $issue --body "This issue has been automatically closed due to 90 days of inactivity. If this is still relevant, please reopen it."
  gh issue close $issue --reason "not planned"
done

CLOSED_COUNT=$(echo "$STALE_ISSUES" | wc -w)
echo "Closed $CLOSED_COUNT stale issues"`
  );
}
