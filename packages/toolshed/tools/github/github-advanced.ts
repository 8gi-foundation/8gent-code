/**
 * Auto-create a GitHub issue for a persistent benchmark failure.
 * Labels with "benchmark-failure" and "automated" for tracking.
 */
export async function createIssueFromFailure(
  benchmark: string,
  error: string,
  score: number
): Promise<string> {
  const title = `Benchmark failure: ${benchmark} (score: ${score.toFixed(2)})`;
  const body = [
    `## Benchmark Failure Report`,
    ``,
    `| Field | Value |`,
    `|-------|-------|`,
    `| **Benchmark** | \`${benchmark}\` |`,
    `| **Score** | ${score.toFixed(4)} |`,
    `| **Date** | ${new Date().toISOString()} |`,
    ``,
    `### Error`,
    `\`\`\``,
    error,
    `\`\`\``,
    ``,
    `---`,
    `*Auto-generated by 8gent benchmark runner*`,
  ].join("\n");

  const escapedTitle = title.replace(/"/g, '\\"');
  const result = run(
    `gh issue create --title "${escapedTitle}" --body "${body.replace(/"/g, '\\"')}" --label "benchmark-failure,automated"`
  );
  return result; // Returns the issue URL
}

/**
 * Update a pinned issue with the latest benchmark scores.
 * Finds or creates a "Benchmark Dashboard" issue.
 */
export async function syncBenchmarkResults(
  scores: Record<string, number>
): Promise<void> {
  const dashboardTitle = "Benchmark Dashboard";

  // Find existing dashboard issue
  const existing = run(
    `gh issue list --search "${dashboardTitle} in:title" --state open --json number,title --limit 1`
  );
  const issues = JSON.parse(existing || "[]");

  const rows = Object.entries(scores)
    .map(([name, score]) => {
      const emoji = score >= 0.8 ? "🟢" : score >= 0.5 ? "🟡" : "🔴";
      return `| ${emoji} ${name} | ${score.toFixed(4)} |`;
    })
    .join("\n");

  const body = [
    `## 8gent Benchmark Scores`,
    ``,
    `*Last updated: ${new Date().toISOString()}*`,
    ``,
    `| Benchmark | Score |`,
    `|-----------|-------|`,
    rows,
    ``,
    `---`,
    `*Auto-updated by 8gent benchmark sync*`,
  ].join("\n");

  if (issues.length > 0) {
    run(
      `gh issue edit ${issues[0].number} --body "${body.replace(/"/g, '\\"')}"`
    );
  } else {
    run(
      `gh issue create --title "${dashboardTitle}" --body "${body.replace(/"/g, '\\"')}" --label "dashboard,automated" --pin`
    );
  }
}

/**
 * Create a GitHub Projects (v2) board.
 */
export async function createProjectBoard(
  name: string,
  columns: string[]
): Promise<string> {
  // Get the repo owner
  const repoInfo = run(`gh repo view --json owner,name`);
  const { owner, name: repoName } = JSON.parse(repoInfo);

  // Create project
  const result = run(
    `gh project create --owner "${owner.login}" --title "${name.replace(/"/g, '\\"')}"`
  );

  // Extract project number from the URL
  const projectNumber = result.match(/\/(\d+)\s*$/)?.[1];
  if (!projectNumber) return result;

  // Add status field columns
  for (const col of columns) {
    try {
      run(
        `gh project field-create ${projectNumber} --owner "${owner.login}" --name "${col}" --data-type TEXT`
      );
    } catch {
      // Column creation might not be supported via CLI; skip silently
    }
  }

  return result;
}

