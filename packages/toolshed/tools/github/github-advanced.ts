// ── PR Workflow ─────────────────────────────────────

/**
 * Create a PR with benchmark results formatted in the body.
 */
export async function createBenchmarkPR(
  branch: string,
  scores: Record<string, number>,
  improvements: string[]
): Promise<string> {
  const scoreRows = Object.entries(scores)
    .map(([name, score]) => `| ${name} | ${score.toFixed(4)} |`)
    .join("\n");

  const improvementList = improvements.map((i) => `- ${i}`).join("\n");

  const body = [
    `## Benchmark Results`,
    ``,
    `| Benchmark | Score |`,
    `|-----------|-------|`,
    scoreRows,
    ``,
    `### Improvements`,
    improvementList || "_No specific improvements noted._",
    ``,
    `---`,
    `*Generated by 8gent benchmark PR workflow*`,
  ].join("\n");

  const title = `bench: update scores (${Object.keys(scores).length} benchmarks)`;
  const result = run(
    `gh pr create --head "${branch}" --title "${title}" --body "${body.replace(/"/g, '\\"')}"`
  );
  return result;
}

/**
 * Add a comment to a pull request.
 */
