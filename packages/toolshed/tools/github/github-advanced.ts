import { execSync } from "child_process";

// ── Helpers ─────────────────────────────────────────

function run(cmd: string, cwd?: string): string {
  try {
    return execSync(cmd, {
      cwd: cwd || process.cwd(),
      encoding: "utf-8",
      timeout: 30000,
    }).trim();
  } catch (err: any) {
    throw new Error(err.stderr?.trim() || err.message);
  }
}

function ghApi(endpoint: string, method = "GET", body?: object): any {
  let cmd = `gh api ${endpoint} --method ${method}`;
  if (body) {
    cmd += ` --input -`;
    return JSON.parse(
      execSync(cmd, {
        input: JSON.stringify(body),
        encoding: "utf-8",
        timeout: 30000,
      }).trim()
    );
  }
  return JSON.parse(run(cmd));
}

