/**
 * Create a public gist with competition/benchmark results.
 */
export async function createResultsGist(
  title: string,
  content: string
): Promise<string> {
  const filename = title.replace(/[^a-zA-Z0-9-_]/g, "-") + ".md";
  // Write content to a temp file, create gist, clean up
  const tmpFile = `/tmp/8gent-gist-${Date.now()}.md`;
  execSync(`cat > "${tmpFile}"`, {
    input: `# ${title}\n\n${content}\n`,
    encoding: "utf-8",
  });

  const result = run(
    `gh gist create "${tmpFile}" --desc "${title.replace(/"/g, '\\"')}" --public`
  );

  execSync(`rm -f "${tmpFile}"`);
  return result; // Returns the gist URL
}

/**
 * Update an existing gist's content.
 */
export async function updateGist(
  gistId: string,
  content: string
): Promise<void> {
  const tmpFile = `/tmp/8gent-gist-update-${Date.now()}.md`;
  execSync(`cat > "${tmpFile}"`, { input: content, encoding: "utf-8" });

  run(`gh gist edit ${gistId} "${tmpFile}"`);
  execSync(`rm -f "${tmpFile}"`);
}

// ── Repository Analysis ─────────────────────────────

/**
 * Get repository stats: commits this week, open PRs/issues, contributors.
 */
export async function getRepoStats(): Promise<RepoStats> {
  const repoJson = run(
    `gh repo view --json name,defaultBranchRef`
  );
  const repo = JSON.parse(repoJson);

  const oneWeekAgo = new Date(Date.now() - 7 * 24 * 60 * 60 * 1000)
    .toISOString()
    .split("T")[0];

  const commitsThisWeek = parseInt(
    run(`git log --oneline --since="${oneWeekAgo}" | wc -l`).trim(),
    10
  );

  const openPRs = parseInt(
    run(`gh pr list --state open --json number | jq length`),
    10
  );

  const openIssues = parseInt(
    run(`gh issue list --state open --json number | jq length`),
    10
  );

  const contributorsRaw = run(
    `git log --since="${oneWeekAgo}" --format='%an' | sort -u`
  );
  const contributors = contributorsRaw
    .split("\n")
    .filter(Boolean);

  return {
    commitsThisWeek,
    openPRs,
    openIssues,
    contributors,
    defaultBranch: repo.defaultBranchRef?.name || "main",
    repoName: repo.name,
  };
}

/**
 * Get branch protection rules for a given branch.
 */
export async function getBranchProtection(branch: string): Promise<any> {
  const repoJson = run(`gh repo view --json nameWithOwner`);
  const { nameWithOwner } = JSON.parse(repoJson);

  try {
    const result = run(
      `gh api repos/${nameWithOwner}/branches/${branch}/protection`
    );
    return JSON.parse(result);
  } catch (err: any) {
    if (err.message.includes("404") || err.message.includes("Not Found")) {
      return { protected: false, message: "No branch protection rules configured" };
    }
    throw err;
  }
}
